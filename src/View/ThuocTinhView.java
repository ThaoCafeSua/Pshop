    private void btnThemDMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemDMActionPerformed
        DanhMuc dm = new DanhMuc();
        String tenDM = txtDM.getText();
        if (tenDM.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Tên Danh Mục");
            return;
        }
        // Kiểm tra xem tên thương hiệu đã tồn tại hay chưa
        if (dmRepo.TonTaiTenDM(tenDM)) {
            JOptionPane.showMessageDialog(this, "Tên danh mục đã tồn tại");
            return;
        }
        dm.setTenDM(txtDM.getText());
        if (rdoDM.isSelected()) {
            dm.setTrangThai(true); // Hoạt động
        } else {
            dm.setTrangThai(false); // Không hoạt động
        }

        if (dmRepo.add(dm) == true) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại ");
        }
        loadDataDM((ArrayList<DanhMuc>) dmRepo.getAll());
        sp.initCbo();
    }//GEN-LAST:event_btnThemDMActionPerformed

