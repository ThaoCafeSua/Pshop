    private void btnXoaTHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaTHActionPerformed
        int index = tblThuongHieu.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Chọn");
            return;
        }
        int chk = JOptionPane.showConfirmDialog(this, "Xác Nhận Xóa");
        if (chk != JOptionPane.YES_OPTION) {
            return;
        }
        ThuongHieu OBJ = lstTH.get(index);
        if (thRepo.Xoa(OBJ.getId())) {
            JOptionPane.showMessageDialog(this, "Xóa Thành Công");
        } else {
            JOptionPane.showMessageDialog(this, " Xóa Thất Bại");
        }
        loadDataTH((ArrayList<ThuongHieu>) thRepo.getAll());
    }//GEN-LAST:event_btnXoaTHActionPerformed

