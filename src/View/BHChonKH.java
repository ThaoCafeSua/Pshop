    private void tbl_ChonKHMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_ChonKHMousePressed

        if (evt.getClickCount() == 2) {  // Xử lý sự kiện nhấp đôi chuột
            int selectedRow = tbl_ChonKH.getSelectedRow();
            if (selectedRow >= 0) {
                String tenKH = tbl_ChonKH.getValueAt(selectedRow, 1).toString();
                String sdt = (tbl_ChonKH.getValueAt(selectedRow, 6).toString());

                // Cập nhật thông tin khách hàng vào panel trong BanHang
                if (BH != null) {
                    BH.updateCustomerInfo(sdt, tenKH);
                }
                this.setVisible(false);  // Đóng cửa sổ chọn khách hàng
            }
        }


    }//GEN-LAST:event_tbl_ChonKHMousePressed

