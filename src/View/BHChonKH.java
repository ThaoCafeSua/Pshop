    public void updateTable(ArrayList<Model_khachHang> customers) {
        mol.setRowCount(0); // Xóa các hàng cũ              
        for (Model_khachHang kh : customers) {
            mol.addRow(new Object[]{kh.getId(), kh.getTen(), kh.getNgaySinh(), kh.isGioiTinh() ? "Nam" : "Nữ", kh.getDiaChi(), kh.getEmail(), kh.getSdt()});
        }
    }
