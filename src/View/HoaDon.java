    public void loadTable(ArrayList<model.Model_HoaDon> t) {
        mol = (DefaultTableModel) tbl_hoaDon.getModel();
        mol.setRowCount(0);
        int stt = 1;

        for (Model_HoaDon hd : t) {
            mol.addRow(new Object[]{hd.getID(), stt++,
                hd.getTenKhachHang(), hd.getTenNhanVien(), hd.getPhuongThucTT(), hd.getMaVoucher(), hd.getTongTien(), hd.getNgayTao(), hd.getNgayThanhToan(), hd.getNgaySua(),
                hd.isTrangThai() ? "Đã Thanh Toán" : "Chưa Thanh Toán"
            });
        }
    }

    public void loadTableHDCT(ArrayList<model.Model_SanPhamCT> t) {
        mol = (DefaultTableModel) tablehdct.getModel();
        mol.setRowCount(0);
        int stt = 1;
        for (model.Model_SanPhamCT hd : t) {
            mol.addRow(new Object[]{hd.getMa(), stt++, hd.getTenSanPham(), hd.getMauSac(), hd.getTenSize(), hd.getSoLuong(), hd.getGiaTien(), hd.getTongTien()
            });
        }
    }

