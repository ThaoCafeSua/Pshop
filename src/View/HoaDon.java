    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        java.util.Date fromDateUtil = txttungay.getDate();
        java.util.Date toDateUtil = txtdenngay.getDate();

        if (fromDateUtil == null) {
            JOptionPane.showMessageDialog(null, "Ngày bắt đầu không được chọn.");
            return;
        }

        if (toDateUtil == null) {
            toDateUtil = new java.util.Date();
        }

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String ngayBD;
        String ngayKT;

        try {
            ngayBD = sdf.format(fromDateUtil);
            ngayKT = sdf.format(toDateUtil);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Có lỗi khi định dạng ngày tháng: " + e.getMessage());
            return;
        }

        if (fromDateUtil.after(toDateUtil)) {
            JOptionPane.showMessageDialog(null, "Ngày bắt đầu không được lớn hơn ngày kết thúc.");
            return;
        }

        if (ngayBD == null || ngayBD.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ngày bắt đầu không hợp lệ.");
            return;
        }

        if (ngayKT == null || ngayKT.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ngày kết thúc không hợp lệ.");
            return;
        }

        try {
            loadTable(rp.loc(ngayBD, ngayKT));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Có lỗi khi lọc dữ liệu: " + e.getMessage());
        }
//        loadTable(result);
    }//GEN-LAST:event_jButton1ActionPerformed

